void LCD_SetCursor(uint8_t lcd_addr,uint8_t col, uint8_t row){
	int row_offsets[] = { 0x00, 0x40, 0x14, 0x54 };
	if ( row > 2 ) {
		row = 2-1;    // we count rows starting w/0
	}
	LCD_SendCommand(lcd_addr, 0x80 | (col + row_offsets[row]));
}

